static const DartCidTable *cid_table_for_version(const char *version) {
	if (!version) {
		return NULL;
	}
	for (int i = 0; cid_tables[i].version; i++) {
		if (!strcmp (cid_tables[i].version, version)) {
			return &cid_tables[i];
		}
	}
	return NULL;
}

static const DartCidTable *cid_table_for_layout(const DartVerLayout *layout) {
	const DartCidTable *table = cid_table_for_version (layout? layout->dart_version: NULL);
	if (table) {
		return table;
	}
	if (layout) {
		for (int i = 0; cid_tables[i].version; i++) {
			const DartCidTable *t = &cid_tables[i];
			if (layout->cid_class == t->cids[DART_CID_CLASS] &&
				layout->cid_function == t->cids[DART_CID_FUNCTION] &&
				layout->cid_code == t->cids[DART_CID_CODE] &&
				layout->cid_string == t->cids[DART_CID_STRING] &&
				layout->num_predefined_cids == t->cids[DART_CID_NUM_PREDEFINED_CIDS]) {
				return t;
			}
		}
	}
	return cid_table_for_version ("3.9.2");
}

const char *dart_cid_kind_name(DartCidKind kind) {
	if (kind >= 0 && kind < DART_CID_KIND_COUNT && cid_kind_names[kind]) {
		return cid_kind_names[kind];
	}
	return "Unknown";
}

int dart_cid_get(const DartVerLayout *layout, DartCidKind kind) {
	if (kind < 0 || kind >= DART_CID_KIND_COUNT) {
		return -1;
	}
	if (layout) {
		switch (kind) {
		case DART_CID_CLASS:
			if (layout->cid_class > 0) {
				return layout->cid_class;
			}
			break;
		case DART_CID_FUNCTION:
			if (layout->cid_function > 0) {
				return layout->cid_function;
			}
			break;
		case DART_CID_CODE:
			if (layout->cid_code > 0) {
				return layout->cid_code;
			}
			break;
		case DART_CID_STRING:
			if (layout->cid_string > 0) {
				return layout->cid_string;
			}
			break;
		case DART_CID_ONE_BYTE_STRING:
			if (layout->cid_one_byte_string > 0) {
				return layout->cid_one_byte_string;
			}
			break;
		case DART_CID_TWO_BYTE_STRING:
			if (layout->cid_two_byte_string > 0) {
				return layout->cid_two_byte_string;
			}
			break;
		case DART_CID_ARRAY:
			if (layout->cid_array > 0) {
				return layout->cid_array;
			}
			break;
		case DART_CID_MINT:
			if (layout->cid_mint > 0) {
				return layout->cid_mint;
			}
			break;
		case DART_CID_OBJECT_POOL:
			if (layout->cid_object_pool > 0) {
				return layout->cid_object_pool;
			}
			break;
		case DART_CID_NUM_PREDEFINED_CIDS:
			if (layout->num_predefined_cids > 0) {
				return layout->num_predefined_cids;
			}
			break;
		default:
			break;
		}
	}
	const DartCidTable *table = cid_table_for_layout (layout);
	const int value = table? table->cids[kind]: 0;
	if (kind == DART_CID_ILLEGAL) {
		return 0;
	}
	return value > 0? value: -1;
}

bool dart_cid_is(const DartVerLayout *layout, int cid, DartCidKind kind) {
	const int value = dart_cid_get (layout, kind);
	return value >= 0 && cid == value;
}

int dart_cid_typed_data_internal_base(const DartVerLayout *layout) {
	const DartCidTable *table = cid_table_for_layout (layout);
	if (table && table->typed_data_internal_base > 0) {
		return table->typed_data_internal_base;
	}
	const int predefined = dart_cid_get (layout, DART_CID_NUM_PREDEFINED_CIDS);
	if (predefined > 170) {
		return predefined - 63;
	}
	if (predefined > 0) {
		return predefined - 48;
	}
	return 112;
}

int dart_cid_typed_data_internal_limit(const DartVerLayout *layout) {
	const DartCidTable *table = cid_table_for_layout (layout);
	if (table && table->typed_data_internal_limit > 0) {
		return table->typed_data_internal_limit;
	}
	const int base = dart_cid_typed_data_internal_base (layout);
	const int stride = dart_cid_typed_data_stride (layout);
	return base + (14 * stride);
}

int dart_cid_typed_data_stride(const DartVerLayout *layout) {
	const DartCidTable *table = cid_table_for_layout (layout);
	if (table && table->typed_data_stride > 0) {
		return table->typed_data_stride;
	}
	return dart_cid_get (layout, DART_CID_NUM_PREDEFINED_CIDS) > 170? 4: 3;
}
